package com.example.projectcryptoapp;

public class RoomDatabase {
    public String name;
    public int num;

    public RoomDatabase(String name, int num) {
        this.name = name;
        this.num = num;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }
}
